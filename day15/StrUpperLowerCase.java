package day15;

public class StrUpperLowerCase {
	public static void main(String[] args) {
		
		
		String str = "java";
		System.out.println(str);
		
		str = str.toUpperCase();
		System.out.println(str);
		str = str.toLowerCase();
		System.out.println(str);
		
		System.out.println("---------");
		
		
		String name = "John Doe";
		System.out.println(name);
		System.out.println(name.toUpperCase());
		System.out.println(name);
	}

}
